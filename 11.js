// Assignment Operator

// | Operator  | Example  |  Same As    |  
// | ----------|----------|------------ |
// | `=`       | `x = 5`  | `x = 5`     |
// | `+=`      | `x += 5` | `x = x + 5` |
// | `-=`      | `x -= 5` | `x = x - 5` |
// | `*=`      | `x *= 5` | `x = x * 5` |
// | `/=`      | `x /= 5` | `x = x / 5` |
// | `%=`      | `x %= 5` | `x = x % 5` |
// | `**=`     | `x **= 5`| `x = x ** 5`|

let box = 10;
box +=5;
box -= 2;
box *= 2;