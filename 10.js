/// 2 + 3 = 5
// + is an operator
// 2 and 3 are operands

// 1. Arithmetic Operator
// |Operator | Description              | Example             |  Result |
// | `+`     | Addition                 | `5 + 2`             | `7`     |
// | `-`     | Subtraction              | `5 - 2`             | `3`     |
// | `*`     | Multiplication           | `5 * 2`             | `10`    |
// | `/`     | Division                 | `5 / 2`             | `1.5`   |
// | `%`     | Modulus (Remainder       | `5 % 2`             | `1`     |
// | `**`    | Exponentiation           | `2 ** 3`            | `8`     |

let a = 10;
let b = 5;
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a ** b);

